package com.hillel.task4;

public class ZaborGazonCalc {
    public static int perimCalc (int length, int width) {
        int perim = (length + width) * 2;
        return perim;
    }
    public static int gardenCalSqr (int length, int width) {
        int square = length * width;
        return square;
    }

}
